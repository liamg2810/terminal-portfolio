	project, with a completly custom scripting language that I will call Zelv
	lang.
</p>

<hr class="w-full h-px mt-6 border-gray-200" />
<h3 class="text-3xl font-medium my-6">My inspiration</h3>

<p class="text-lg">
	The inspiration for this project mainly came from the time I used a BBC
	Microcomputer a few weeks before writing this (09/07/2025). I was playing
	arond in BASIC and found it to seem really simple and decided to take on the
	challenge of writing my own language. I tried to keep the syntax as close to
	BASIC as possible, while also making it my own language, and I believe I
	have done that quite well.
</p>

