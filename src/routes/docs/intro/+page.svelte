<h1 class="text-5xl font-bold pt-3">Introduction</h1>

<p class="text-lg mt-6">
	In this chapter, I will be introducing basic concepts when writing in Zelv
	lang. In this chapter, you will learn how to write, save, and load code.
</p>

<br />

<h2 class="text-3xl font-bold py-3">Writing Code</h2>

<p class="text-lg">
	To write code, you must first write the line number, followed by a space,
	and then finally the line of code you wish to write. For example, to output
	"Hello, World!" to the terminal, you would write:
	<br />
	<code>10 print "Hello, World!"</code>
	<br />
	You can write as many lines as you want, and they will be executed starting from
	line 1.
	<br />
	If you wish to renumber the lines, which will space each line out by 10, you
	can simply run the command
	<code>renumber</code>
	<br />
	To execute the code you have written, run the command
	<code>run</code>.
</p>

<h2 class="text-3xl font-bold py-3">Saving Code</h2>

<p class="text-lg w-full">
	To save your code to a text file, you use the command
	<code>save &lt;filename&gt;</code>. Once you have saved your code, you can
	load it back in at any time using the command <code>load</code>. This will
	open a file diaglog where you can select the previously saved code, or
	someone elses.
</p>

<h2 class="text-3xl font-bold py-3">Tips</h2>

<p class="text-lg w-full">
	You can use <kbd>Esc</kbd> to terminate execution of the program if you ever
	get stuck.
	<br />
	You can also use <kbd>Ctrl</kbd> + <kbd>C</kbd> to clear the current line you
	are writing.
</p>
