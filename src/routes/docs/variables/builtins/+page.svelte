<h1 class="text-5xl font-bold pt-3">Variables</h1>

<h2 class="text-3xl font-bold py-3">Built-in functions</h2>

<!-- round(x) floor(x) ceil(x) exists(x) typeof(x) -->

<span class="text-lg w-full">
	There are a few built-in functions that you can use to manipulate variables.
	<br />
	These functions are:
	<ul class="list-disc ml-6">
		<li>
			<code>round(x)</code> - Rounds the number <code>x</code> to the nearest
			integer.
		</li>
		<li>
			<code>floor(x)</code> - Rounds the number <code>x</code> down to the
			nearest integer.
		</li>
		<li>
			<code>ceil(x)</code> - Rounds the number <code>x</code> up to the nearest
			integer.
		</li>
		<li>
			<code>exists(x)</code> - Returns <code>"true"</code> if the variable
			<code>x</code>
			exists, otherwise returns <code>"false"</code>.
		</li>
		<li>
			<code>typeof(x)</code> - Returns the type of the variable
			<code>x</code> as a string. Either <code>"number"</code> or
			<code>"string"</code>.
		</li>
	</ul>
</span>
